function expandEntries(entries: CompactEntry[], level: CEFRLevel, prefix: string): VocabWord[] {
  return entries.map((entry, i) => {
    const [german, english, category, frequency, article, plural] = entry;
    return {
      id: `${prefix}-ext-${i + 1}`,
      level,
      german,
      english,
      ...(article && article !== "" ? { article: article as "der" | "die" | "das" } : {}),
      ...(plural && plural !== "" ? { plural } : {}),
      example: { de: "", en: "" }, // basic entries get example from AI enrichment
      category,
      frequency,
      tags: ["extended-bank"],
    } as VocabWord;
  });
}

