// Helper functions
export function getConjugationsByLevel(level: "A1" | "A2" | "B1" | "B2"): VerbConjugation[] {
  const order = { A1: 0, A2: 1, B1: 2, B2: 3 };
  return VERB_CONJUGATIONS.filter((v) => order[v.level] <= order[level]);
}

export function findConjugation(infinitive: string): VerbConjugation | undefined {
  return VERB_CONJUGATIONS.find((v) => v.infinitive.toLowerCase() === infinitive.toLowerCase());
}
